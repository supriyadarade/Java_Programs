package Ex_10_For_Loop;

public class Program_102_For_If_Loop  {

    public static void main(String[] args) {

        for (int supriya = 0 ; supriya < 18 ; supriya++) { // 0 to 17 - 18 times it will run

            if (supriya > 15) {
                System.out.println("I will get a IPhone from the satish");
            }
            else {
                System.out.println("No Gift");
            }
        }
    }
}
