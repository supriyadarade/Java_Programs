package Ex_10_For_Loop;

public class Program_109_For_Loop_4 {

    public static void main(String[] args) {

        for (int i = 0 ; i < 10 ; i++) { // 0 to 9 --- 10 times will it execute
            System.out.println(i);
        }

        for (int i = 1 ; i <= 10 ; i++) { // 1 to 10 --- 11 times will it execute
            System.out.println(i);
        }
    }
}
