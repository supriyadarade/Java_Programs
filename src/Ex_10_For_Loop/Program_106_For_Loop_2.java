package Ex_10_For_Loop;

public class Program_106_For_Loop_2 {

    public static void main(String[] args) {

        // You can use any other data type like int, long, etc.

        for (long i = 0l ; i < 10 ; i++) {
            System.out.println(i);
        }

        for (float f = 0.0f ; f < 10.89 ; f++) {
            System.out.println("Hi, float" + f);
        }

        // not recommended for long and float
    }
}
