package Ex_03_Literals;

public class Program_035_Non_Primitive_Literals {

    public static void main(String[] args) {

        // Primitive data type  - Defined by java guy
        // Max, Min, Size defined

        byte b = 10; // 1 Byte = 8 Bits
        int age  = 65; // 4 Byte = 32 Bits

        // Non Primitive (Defined by Users), Reference data Types
        // No Size, Max, Min
        // Byte - 8, Bits - 64
        String name = "Supriya"; // String is a bunch of characters
        int[] arrays_of_items = new int[10];

        // String, arrays, Classes, enums....
    }
}
