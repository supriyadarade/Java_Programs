package Ex_07_Increment_Decrement;

public class Program_074_Advance_ID {

    public static void main(String[] args) {

        int a = 10;
        System.out.println(a++ + a);
        // A + B
        // A -> a++  -> Exp A -> 10  -> a++ means a -> 11
        // B-> a -> Exp B -> 11 ,
        // A+B -> ExpA + ExpB -> 10+11
    }
}
