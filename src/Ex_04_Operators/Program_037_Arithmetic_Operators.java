package Ex_04_Operators;

public class Program_037_Arithmetic_Operators {

    public static void main(String[] args) {

        // Arithmetic Operators
        // [+] - Addition
        // [-] - Subtraction
        // [*] - multiplication
        // [/] - Division
        // [%] - Modulus

        int a = 20;
        int b = 3;
        float c = 3.0f;
        // float c = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(a/c);

        System.out.println("a/c"); // it will show you the string


    }
}
