package Ex_04_Operators;

public class Program_045_Equal_To_Operator {

    public static void main(String[] args) {

        //  ==  -  Compare the both values
        System.out.println(10 == 10);
        System.out.println(10 < 10);
        System.out.println(10 == 20);
        System.out.println(10 >= 10);
    }
}
