package Ex_09_Switch_Case;

public class Program_095_Interview_QnA {

    public static void main(String[] args) {

        char code = 'C';

        switch (code) {

            case 'A' :
                System.out.println("65");
                break;

            case 'B' :
                System.out.println("66");

            default :
                System.out.println("Default");
                break;
        }
    }
}
