package Ex_09_Switch_Case;

public class Program_094_Interview_QnA {

    public static void main(String[] args) {

        int a = 11;

        switch (-1) {
            // the switch value will get matched with the case value

            default :
                System.out.println("Default");
                break;

            case -1 :
                System.out.println("10");
                break;

            case  9 :
                System.out.println("9");
                break;
        }
    }
}
